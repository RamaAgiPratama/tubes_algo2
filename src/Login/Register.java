    private void registerkelompok1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerkelompok1ActionPerformed
        // TODO add your handling code here:
        String id = idkelompok1.getText().toString().trim();
        String nama = namakelompok1.getText().toString().trim();
        String username = usernamekelompok1.getText().toString().trim();
        String password = passwordkelompok1.getText().toString().trim();
        String conPassword = conkelompok1.getText().toString().trim();
        
        
        if(!password.equals(conPassword)){
            JOptionPane.showMessageDialog(null,"Password Salah","TOKO IBU OTIH",JOptionPane.WARNING_MESSAGE);
        }else if (password.equals("") || username.equals("")){
            JOptionPane.showMessageDialog(null,"Username dan Password Tidak Diketahui","TOKO IBU OTIH",JOptionPane.WARNING_MESSAGE);
        }else{
            try{
                Connection c = koneksi.getKoneksi();
                String sql = "INSERT INTO t_login VALUES (?, ?, ?)";
                PreparedStatement p = c.prepareStatement(sql);
                p.setString(1, id);
                p.setString(2, username);
                p.setString(3, password);
                p.executeUpdate();
                p.close();
                JOptionPane.showMessageDialog(null, "Buat Akun Berhasil","TOKO IBU OTIH",JOptionPane.INFORMATION_MESSAGE);
            }catch(SQLException e){
                System.out.println("Error");
            }finally{
                this.dispose();
                Login a = new Login();
                a.setVisible(true);
            }
        }
    }//GEN-LAST:event_registerkelompok1ActionPerformed

